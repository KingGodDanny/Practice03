package com.javaex.practice;

public class Ex08 {

	public static void main(String[] args) {
		
		int y,dan;
		
		for (y=1; y<10; y++) {
			for (dan=2; dan<10; dan++) {
				
				System.out.print(dan + "*" + y + "=" + dan*y + "	");
				
			}
			
			System.out.println("");
			
		}
		
	}

}
